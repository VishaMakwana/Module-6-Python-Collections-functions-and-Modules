# Write a Python program to access values between index 1 and 5 in a tuple

tuple=(10, "Hello", 3.14, True, 50, "Python")

print("index in 0",tuple[0])
print("index in 1",tuple[1])
print("index in 2",tuple[2])
print("index in 3",tuple[3])
print("index in 4",tuple[4])
print("index in 5",tuple[5])

print("index between 1 and 5",tuple[1:5])